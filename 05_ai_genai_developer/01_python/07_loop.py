# loops in python 


# range function in python
range(5) # it will return 0,1,2,3,4
range(1,5) # it will return 1,2,3,4
range(1,10,2) # it will return 1,3,5,7,9
range(10,1,-1) # it will return 10,9,8,7,6,5,4,3,2

# for loop in python
for i in range(5):
    print(i) # it will print 0,1,2,3,4
    

# loop through string in python
a= "hello"
for i in a:
    print(i) # it will print h,e,l,l,o
    
    
# while loop in python
count = 0
while count < 5:
    print(count) # it will print 0,1,2,3,4
    count += 1


