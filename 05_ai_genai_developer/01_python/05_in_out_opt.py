# operators :
print(10+5) # addition
print(10-5) # subtraction
print(10*5) # multiplication
print(10/5) # division
print(10%5) # modulus
print(10//5) # floor division
print(10**5) # exponentiation
print(10>5) # greater than
print(10<5) # less than
print(10>=5) # greater than or equal to
print(10<=5) # less than or equal to
print(10==5) # equal to
print(10!=5) # not equal to
print(10 and 5) # logical and
print(10 or 5) # logical or
print(not 10) # logical not
print(10 is 5) # identity operator
print(10 is not 5) # identity operator
print(10 in [1,2,3,4,5]) # membership operator
print(10 not in [1,2,3,4,5]) # membership operator
print(10 & 5) # bitwise and
print(10 | 5) # bitwise or
print(10 ^ 5) # bitwise xor
print(~10) # bitwise not
print(10 << 5) # bitwise left shift
print(10 >> 5) # bitwise right shift
print(10 + 5 * 2) # operator precedence
print((10 + 5) * 2) # operator precedence
print(10 + 5 * 2 ** 2) # operator precedence
